import numpy as np

# This code divides elements of one array by elements of another array

array1 = np.array([10, 20, 30])  # np.array creates a NumPy array
array2 = np.array([2, 4, 6])  # np.array creates a NumPy array

result = array1 / array2

print(result)  # print displays the result

# Output: [5. 5. 5.]
